package uk.org.mygrid.sogsa.sbs;


public class SemanticBindingException extends Exception {

	public SemanticBindingException() {
		super();
	}
	
	public SemanticBindingException(String message) {
		super(message);
	}

	public SemanticBindingException(String message, Throwable cause) {
		super(message, cause);
	}

	public SemanticBindingException(Throwable cause) {
		super(cause);
	}

}
