package net.sf.taverna.ocula.action;

import java.util.Map;

import net.sf.taverna.ocula.Ocula;

/**
 * Users of the ProcessFieldsAction are required to implement this interface.
 * It provides a sole method that processes the data generated by the action.
 * 
 * @author Ismael Juma (ismael@juma.me.uk)
 *
 */
public interface Processor {

    /**
     * Processes the data in <code>map</code>.
     * @param map A Map where the key is the name attribute of &lt;text&gt; and
     * 		  the value is the text contained in it when the action was
     * 		  initiated.
     * @param ocula Ocula instance.
     * @throws Exception if a problem ocurred while processing the map.
     */
    public void process(Map map, Ocula ocula) throws Exception;
}
