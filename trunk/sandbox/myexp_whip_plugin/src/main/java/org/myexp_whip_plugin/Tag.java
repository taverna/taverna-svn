package org.myexp_whip_plugin;

public class Tag extends Resource {
	
	private String tagName;
	
	private int count;

	public String getTagName() {
		return tagName;
	}

	public void setTagName(String tagName) {
		this.tagName = tagName;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
}
