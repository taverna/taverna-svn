package net.sf.taverna.dalec.exceptions;

/**
 * Javadocs go here.
 *
 * @author Tony Burdett date: 12-Jul-2005
 */
public class IncorrectlyNamedProcessorException extends Throwable
{
    public IncorrectlyNamedProcessorException()
    {
        super();
    }

    public IncorrectlyNamedProcessorException(String message)
    {
        super(message);
    }

    public IncorrectlyNamedProcessorException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public IncorrectlyNamedProcessorException(Throwable cause)
    {
        super(cause);
    }
}
